void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	//---Taking Average of calculated temperature---
	Temperature_Array[i]		= ADC_Values[0] * 3300 / 4095 / 10;			// ( * 3300 / 4095) converts it to mV, ( / 10) is slope of LM35
	i++;
	if(i == 5)
	{i = 0;}

	Temperature = ( Temperature_Array[0] + Temperature_Array[1] + Temperature_Array[2]+ Temperature_Array[3]+ Temperature_Array[4] ) / 5;


	//---storing light intensity value---
	Light_intensity = ADC_Values[1] * 100 / 4095;				// converts the 12-bit number into a number in percent
}
